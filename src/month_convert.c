/* Function to convert month*/

char * month_convert(int cmon) {
    char * smon;

    switch (cmon) {

    case 1:
        smon = "Jan";
        return smon;
        break;

    case 2:
        smon = "Feb";
        return smon;
        break;

    case 3:
        smon = "Mar";
        return smon;
        break;

    case 4:
        smon = "Apr";
        return smon;
        break;

    case 5:
        smon = "May";
        return smon;
        break;

    case 6:
        smon = "Jun";
        return smon;
        break;

    case 7:
        smon = "Jul";
        return smon;
        break;

    case 8:
        smon = "Aug";
        return smon;
        break;

    case 9:
        smon = "Sep";
        return smon;
        break;

    case 10:
        smon = "Oct";
        return smon;
        break;

    case 11:
        smon = "Nov";
        return smon;
        break;

    case 12:
        smon = "Dec";
        return smon;
        break;
    }
}
